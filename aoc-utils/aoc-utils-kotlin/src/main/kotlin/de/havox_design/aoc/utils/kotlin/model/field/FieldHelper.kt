package de.havox_design.aoc.utils.kotlin.model.field

/**********************************************************************************************************************
 * Generics                                                                                                           *
 **********************************************************************************************************************/
fun <T> T.print() =
    this
        .also { println(it) }

/**********************************************************************************************************************
 * List<List>                                                                                                         *
 **********************************************************************************************************************/
fun <T> List<List<T>>.toField() =
    Field(this.reversed())
